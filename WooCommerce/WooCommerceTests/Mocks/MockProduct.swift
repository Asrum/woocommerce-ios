import Yosemite

extension Product {
    /// Creates a product with default properties.
    static func create(siteID: Int64 = 1,
                       productID: Int64 = 2,
                       name: String = "Woo super cool shiny product",
                       slug: String = "",
                       permalink: String = "",
                       date: Date = Date(),
                       dateCreated: Date = Date(),
                       dateModified: Date? = Date(),
                       dateOnSaleStart: Date? = Date(),
                       dateOnSaleEnd: Date? = Date(),
                       productTypeKey: String = ProductType.variable.rawValue,
                       statusKey: String = ProductStatus.draft.rawValue,
                       featured: Bool = false,
                       catalogVisibilityKey: String = ProductCatalogVisibility.hidden.rawValue,
                       fullDescription: String? = "",
                       shortDescription: String? = "",
                       sku: String? = "sku",
                       price: String = "0",
                       regularPrice: String? = "32.5",
                       salePrice: String? = "17.6",
                       onSale: Bool = false,
                       purchasable: Bool = true,
                       totalSales: Int = 0,
                       virtual: Bool = false,
                       downloadable: Bool = false,
                       downloads: [ProductDownload] = [],
                       downloadLimit: Int64 = -1,
                       downloadExpiry: Int64 = -1,
                       buttonText: String = "",
                       externalURL: String? = "",
                       taxStatusKey: String = ProductTaxStatus.taxable.rawValue,
                       taxClass: String? = "",
                       manageStock: Bool = true,
                       stockQuantity: Decimal? = 32,
                       stockStatusKey: String = ProductStockStatus.inStock.rawValue,
                       backordersKey: String = ProductBackordersSetting.allowed.rawValue,
                       backordersAllowed: Bool = false,
                       backordered: Bool = false,
                       soldIndividually: Bool = true,
                       weight: String? = "2.9",
                       dimensions: ProductDimensions = ProductDimensions(length: "12", width: "26", height: "16"),
                       shippingRequired: Bool = false,
                       shippingTaxable: Bool = false,
                       shippingClass: String? = "",
                       shippingClassID: Int64 = 0,
                       productShippingClass: ProductShippingClass? = nil,
                       reviewsAllowed: Bool = true,
                       averageRating: String = "4.30",
                       ratingCount: Int = 23,
                       relatedIDs: [Int64] = [31, 22, 369, 414, 56],
                       upsellIDs: [Int64] = [99, 1234566],
                       crossSellIDs: [Int64] = [1234, 234234, 3],
                       parentID: Int64 = 0,
                       purchaseNote: String? = "Thank you!",
                       categories: [ProductCategory] = [],
                       tags: [ProductTag] = [],
                       images: [ProductImage] = [],
                       attributes: [ProductAttribute] = [],
                       defaultAttributes: [ProductDefaultAttribute] = [],
                       variations: [Int64] = [],
                       groupedProducts: [Int64] = [],
                       menuOrder: Int = 0,
                       addOns: [ProductAddOn] = []) -> Product {
        Product(siteID: siteID,
                productID: productID,
                name: name,
                slug: slug,
                permalink: permalink,
                date: date,
                dateCreated: dateCreated,
                dateModified: dateModified,
                dateOnSaleStart: dateOnSaleStart,
                dateOnSaleEnd: dateOnSaleEnd,
                productTypeKey: productTypeKey,
                statusKey: statusKey,
                featured: featured,
                catalogVisibilityKey: catalogVisibilityKey,
                fullDescription: fullDescription,
                shortDescription: shortDescription,
                sku: sku,
                price: price,
                regularPrice: regularPrice,
                salePrice: salePrice,
                onSale: onSale,
                purchasable: purchasable,
                totalSales: totalSales,
                virtual: virtual,
                downloadable: downloadable,
                downloads: downloads,
                downloadLimit: downloadLimit,
                downloadExpiry: downloadExpiry,
                buttonText: buttonText,
                externalURL: externalURL,
                taxStatusKey: taxStatusKey,
                taxClass: taxClass,
                manageStock: manageStock,
                stockQuantity: stockQuantity,
                stockStatusKey: stockStatusKey,
                backordersKey: backordersKey,
                backordersAllowed: backordersAllowed,
                backordered: backordered,
                soldIndividually: soldIndividually,
                weight: weight,
                dimensions: dimensions,
                shippingRequired: shippingRequired,
                shippingTaxable: shippingTaxable,
                shippingClass: shippingClass,
                shippingClassID: shippingClassID,
                productShippingClass: productShippingClass,
                reviewsAllowed: reviewsAllowed,
                averageRating: averageRating,
                ratingCount: ratingCount,
                relatedIDs: relatedIDs,
                upsellIDs: upsellIDs,
                crossSellIDs: crossSellIDs,
                parentID: parentID,
                purchaseNote: purchaseNote,
                categories: categories,
                tags: tags,
                images: images,
                attributes: attributes,
                defaultAttributes: defaultAttributes,
                variations: variations,
                groupedProducts: groupedProducts,
                menuOrder: menuOrder,
                addOns: addOns)
    }
}
